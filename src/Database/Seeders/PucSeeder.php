    public function run(): void
    {
        $classes = AccountClass::pluck('id', 'code')->toArray();

        $classNature = [
            '1'=>'DEBIT', '2'=>'CREDIT','3'=>'CREDIT',
            '4'=>'CREDIT','5'=>'DEBIT', '6'=>'DEBIT',
            '7'=>'DEBIT', '8'=>'DEBIT', '9'=>'CREDIT',
        ];

        $created = []; // code => id

        foreach ($this->accounts() as [$code, $name]) {
            if (isset($created[$code])) continue;

            $first   = $code[0];
            $classId = $classes[$first] ?? null;
            if (!$classId) continue;

            $nature = $classNature[$first];

            $type     = strlen($code) === 2 ? 'MAYOR' : 'MOVIMIENTO';
            $parentId = $this->parentId($code, $created);

            $account = Account::create([
                'parent_id'    => $parentId,
                'class_id'     => $classId,
                'code'         => $code,
                'name'         => trim($name),
                'nature'       => $nature,
                'account_type' => $type,
                'active'       => true,
            ]);

            $created[$code] = $account->id;
        }

        $this->command?->info('PUC: '.count($created).' cuentas creadas.');
    }

    private function parentId(string $code, array $created): ?int
    {
        foreach (match(strlen($code)) {
            4 => [2],
            6 => [4, 2],
            8 => [6, 4, 2],
            default => [],
        } as $len) {
            $key = substr($code, 0, $len);
            if (isset($created[$key])) return $created[$key];
        }
        return null;
    }

